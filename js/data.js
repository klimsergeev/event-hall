/* Ценовые тиры сеанса (цвет ↔ цена), по возрастанию цены — для легенды и модели мест. */
export function sessionTiers(session) {
    return TIER_COLORS.map((color, i) => ({ color, price: session.prices[i] }));
}

/* Минимальная цена сеанса (для чипа «от X ₽»). Тиры отсортированы по возрастанию,
   поэтому минимум = prices[0]. */
export function sessionMin(session) {
    return session.prices[0];
}

/* Формат цены для UI: «1 500 ₽» (неразрывные пробелы-разряды). */
export function formatPrice(n) {
    return n.toLocaleString('ru-RU') + ' ₽';
}

